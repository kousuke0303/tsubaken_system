$(function() {
  var el = document.getElementById("sortable-instructions");
  if (el != null) {
    var sortable = Sortable.create(el, {
      animation: 150,
      delay: 100,
      onUpdate: function(evt) {
        $.ajax({
          type: "PATCH",
          url: "/employees/estimate_matters/#{estimate_matter.id}/instructions/sort",
          cache: false,
          data: { from: evt.oldIndex, 
                  to: evt.newIndex, 
                  remote: true }
        });
      }
    });
  }
});
