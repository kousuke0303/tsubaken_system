$(function() {
  var el = document.getElementById("sortable-matter-tasks");
  if (el != null) {
    var sortable = Sortable.create(el, {
      animation: 150,
      delay: 100,
      onUpdate: function(evt) {
        $.ajax({
          type: "PATCH",
          url: "/employees/settings/tasks/sort",
          cache: false,
          data: { from: evt.oldIndex, 
                  to: evt.newIndex, 
                  remote: true }
        });
      }
    });
  }
  
});

